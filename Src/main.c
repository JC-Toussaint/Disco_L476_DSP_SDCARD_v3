void generateArray(int16_t* AUDIO_SAMPLE){
	//*IF YOU CHANGE THESE PARAMETERS, YOU MUST ALSO CHANGE THE ARRAY SIZE OF SAMPLES*//
	int Amp = 32767; //amplitude of wave (within 16bit signed integer)
	Amp=2000;
	int SR = 44000; //sample rate of I2S channel
	int freq = 400; //frequency of sine wave you want to produce
	int i;
	for(i=0; i < (2*SR/freq); i++){ //mult SR/Freq by 2 b/c of two channels
		if(i%2){
			AUDIO_SAMPLE[i] = (int16_t) Amp*sin(2*M_PI*(i/(2.0*SR/freq))); //right channel
		}
		else
			AUDIO_SAMPLE[i]= (int16_t) Amp*sin(2*M_PI*(i/(4.0*SR/freq)));; //left channel
	}
}

